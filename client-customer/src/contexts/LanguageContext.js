// Language Context
const LanguageContext = createContext();

// Language Provider
export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState('vi'); // Default to Vietnamese

  const t = (key) => {
    return translations[language][key] || key;
  };

  const changeLanguage = (lang) => {
    setLanguage(lang);
    localStorage.setItem('panj_language', lang);
  };

  // Load language from localStorage on mount
  React.useEffect(() => {
    const savedLanguage = localStorage.getItem('panj_language');
    if (savedLanguage && (savedLanguage === 'en' || savedLanguage === 'vi')) {
      setLanguage(savedLanguage);
    }
  }, []);

  const value = {
    language,
    setLanguage: changeLanguage,
    t,
    translations
  };

  return (
    <LanguageContext.Provider value={value}>
      {children}
    </LanguageContext.Provider>
  );
};

// Hook to use language context
export const useLanguage = () => {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
};

// HOC to provide language context to class components
export const withLanguage = (WrappedComponent) => {
  return React.forwardRef((props, ref) => {
    const languageContext = useLanguage();
    
    return (
      <WrappedComponent
        ref={ref}
        {...props}
        language={languageContext.language}
        setLanguage={languageContext.setLanguage}
        t={languageContext.t}
        translations={languageContext.translations}
      />
    );
  });
};

export default LanguageContext;
